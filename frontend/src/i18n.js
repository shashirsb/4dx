export function t(key, locale = 'en') {
  const pack = messages[locale] || messages.en;
  return pack[key] || messages.en[key] || key;
}

export function pageMeta(view, locale = 'en') {
  const map = {
    overview: ['overview', 'overviewSub'],
    projects: ['projects', 'projectsSub'],
    ministries: ['ministries', 'ministriesSub'],
    scoreboard: ['scoreboard', 'scoreboardSub'],
    workflow: ['workflow', 'workflowSub'],
    evidence: ['evidence', 'evidenceSub'],
    decisions: ['decisions', 'decisionsSub'],
    admin: ['admin', 'adminSub'],
  };
  const keys = map[view] || ['appName', 'tagline'];
  return [t(keys[0], locale), t(keys[1], locale)];
}

export function deptLabel(orgType, locale = 'en') {
  const pack = messages[locale] || messages.en;
  if (orgType === 'enterprise') return pack.businessUnits;
  if (orgType === 'public_sector') return pack.departments;
  return pack.ministries;
}

export function formatMoney(amount, currency = 'USD', locale = 'en') {
  try {
    return new Intl.NumberFormat(locale === 'sg' ? 'en-SG' : locale, { style: 'currency', currency, maximumFractionDigits: 0 }).format(amount);
  } catch {
    return `${currency} ${amount}`;
  }
}

export function formatDate(value, locale = 'en') {
  const time = Date.parse(value || '');
  if (Number.isNaN(time)) return value || '—';
  const loc = locale === 'sg' ? 'en-SG' : locale === 'zh' ? 'zh-CN' : locale;
  try {
    return new Intl.DateTimeFormat(loc, { day: '2-digit', month: 'short', year: 'numeric' }).format(new Date(time));
  } catch {
    return value;
  }
}
